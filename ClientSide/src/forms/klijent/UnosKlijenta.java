    private void btnUnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnosActionPerformed
        if(!setujKlijenta()) {
            return;
        }

        try {
            Controler.getInstance().sacuvajKlijenta(k);
            JOptionPane.showMessageDialog(null, "Sistem je zapamtio klijenta.");

            Object[] options = {"Da", "Ne"};

            if (JOptionPane.showOptionDialog(null, "Da li zelite da unesete novog klijenta?", "Novi klijent",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
                    null, options, options[0]) == 0) {
                UnosKlijenta lup = new UnosKlijenta(null, true);
                this.dispose();
                lup.setVisible(true);
            } else {
                this.dispose();
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne može da zapamti klijenta.");
            System.exit(0);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne može da zapamti klijenta.");
        }
    }//GEN-LAST:event_btnUnosActionPerformed

