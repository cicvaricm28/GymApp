    private void prepareCb() {
        cbVrste.removeAllItems();
        try {
            ArrayList<VrstaTreninga> vrste = Controler.getInstance().vratiVrste();
            for (VrstaTreninga vrstaTreninga : vrste) {
                cbVrste.addItem(vrstaTreninga);
            }
        } catch (Exception ex) {
            Logger.getLogger(UnosKlijenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    private boolean setujKlijenta() {
        if (
                txtIme.getText().isEmpty() ||
                txtPrezime.getText().isEmpty() ||
                txtKontakt.getText().isEmpty() ||
                txtJmbg.getText().isEmpty()
                
        ) {
            JOptionPane.showMessageDialog(null, "Popunite sva polja!");
            return false;
        }
        k = new Klijent(0, txtIme.getText(), txtPrezime.getText(), txtJmbg.getText(), txtKontakt.getText(), (VrstaTreninga) cbVrste.getSelectedItem());
        
        return true;
    }

    private void prikaziKlijenta(Klijent k) {
        cbVrste.removeAllItems();
        cbVrste.addItem(k.getVrstaTreninga().getNazivVrsteTreninga());
        cbVrste.setEnabled(false);
        
        txtIme.setText(k.getIme());
        txtPrezime.setText(k.getPrezime());
        txtJmbg.setText(k.getJmbg());
        txtKontakt.setText(k.getKontakt());
        
        txtIme.setEnabled(false);
        txtPrezime.setEnabled(false);
        txtJmbg.setEnabled(false);
        txtKontakt.setEnabled(false);
        btnUnos.setEnabled(false);
    }
