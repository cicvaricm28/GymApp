    private void setUpCb() {
        cbMesec.removeAllItems();
        cbMesec.addItem("Januar");
        cbMesec.addItem("Februar");
        cbMesec.addItem("Mart");
        cbMesec.addItem("April");
        cbMesec.addItem("Jun");
        cbMesec.addItem("Jul");
        cbMesec.addItem("Avgust");
        cbMesec.addItem("Septembar");
        cbMesec.addItem("Oktobar");
        cbMesec.addItem("Novembar");
        cbMesec.addItem("Decembar");
        
        
        cbVrsteTreninga.removeAllItems();
        try {
            ArrayList<VrstaTreninga> vrste = Controler.getInstance().vratiVrste();
            for (VrstaTreninga vrstaTreninga : vrste) {
                cbVrsteTreninga.addItem(vrstaTreninga);
            }
        } catch (Exception ex) {
            Logger.getLogger(UnosKlijenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        TableModelStavke t = new TableModelStavke();
        tblStavke.setModel(t);
        
    }

    private boolean setujStavku() {
        if (
                txtNazivStavke.getText().isEmpty() 
                
                
        ){ 
            JOptionPane.showMessageDialog(null, "Popunite sva polja!");
            return false;
        }
        
        StavkaRezimaTreninga s = new StavkaRezimaTreninga(1, null, txtNazivStavke.getText(), Session.getInstance().getCurrentUser(), (VrstaTreninga) cbVrsteTreninga.getSelectedItem());
        stavke.add(s);
        TableModelStavke tms = (TableModelStavke) tblStavke.getModel();
        tms.dodajStavku(s);
        return true;
        } 

    private boolean setujRezim() {
        if (
                txtOpis.getText().isEmpty() 
                
                
        ){ 
            JOptionPane.showMessageDialog(null, "Popunite sva polja!");
            return false;
        }
        rezim = new RezimTreninga(1, txtOpis.getText(), (String) cbMesec.getSelectedItem());
        
        
        return true;
    }
