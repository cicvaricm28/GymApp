    private void btnSacuvajRezimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajRezimActionPerformed
        if(!setujRezim()){
            return;
        }
        try {
            rezim.setStavke(stavke);
            Controler.getInstance().sacuvajRezim(rezim);
            JOptionPane.showMessageDialog(null, "Sistem je zapamtio rezim.");
//            this.dispose();
            Object[] options = {"Da", "Ne"};

            if (JOptionPane.showOptionDialog(null, "Da li zelite da unesete novi rezim?", "Novi rezim",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
                    null, options, options[0]) == 0) {
                UnesiStavku lup = new UnesiStavku(null, true);
                this.dispose();
                lup.setVisible(true);
            } else {
                this.dispose();
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne može da zapamti rezim.");
            System.exit(0);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne može da zapamti rezim.");
        }
        
    }//GEN-LAST:event_btnSacuvajRezimActionPerformed

